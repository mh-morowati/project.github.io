class House {
  constructor(
    adTitle,
    agencName,
    picAddress,
    adType,
    location,
    price,
    meterage,
    rooms,
    year,
    floor,
    wareHouse,
    elevator,
    parking
  ) {
    this.adTitle = adTitle;
    this.agencName = agencName;
    this.picAddress = picAddress;
    this.adType = adType;
    this.location = location;
    this.price = price;
    this.meterage = meterage;
    this.rooms = rooms;
    this.year = year;
    this.floor = floor;
    this.wareHouse = wareHouse;
    this.elevator = elevator;
    this.parking = parking;
  }
}
